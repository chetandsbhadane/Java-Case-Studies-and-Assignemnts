
public class Rectangle {

	private float l;
	private float b;
	private float aRect;
	private float p;
	public Rectangle(float l, float b, float aRect, float p) {
		super();
		this.l = l;
		this.b = b;
		this.aRect = aRect;
		this.p = p;
	}
	public float getL() {
		return l;
	}
	public void setL(float l) {
		this.l = l;
	}
	public float getB() {
		return b;
	}
	public void setB(float b) {
		this.b = b;
	}
	public float getaRect() {
		return aRect;
	}
	public void setaRect(float aRect) {
		this.aRect = aRect;
	}
	public float getP() {
		return p;
	}
	public void setP(float p) {
		this.p = p;
	}
	
	
}
